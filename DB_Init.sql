create view [dbo].[v_Ranking]
AS
SELECT gp.[Name]
, SUM(gp.Score) AS Score
, RANK() OVER (ORDER BY SUM(gp.Score) DESC) AS Rank  
FROM dbo.GamePlayer gp
INNER JOIN dbo.game g ON gp.GameId = g.Id
WHERE g.CanceledBy IS NULL OR g.CanceledBy = ''
GROUP BY gp.[Name]
